class  PrimitiveTypes
{
    static byte bytte;
    static short shortt;
    static int intt;
    static long longg;
    static float floatt;
    static double doublee;
    static char charr;
    static boolean b;
    public static void main(String args[]) 
    {
        System.out.println("Byte :"+bytte);
        System.out.println("Short :"+shortt);
        System.out.println("Int :"+intt);
        System.out.println("Long :"+longg);
        System.out.println("Float :"+floatt);
        System.out.println("Double :"+doublee);
        System.out.println("Char :"+charr);
        System.out.println("Boolean :"+b);
    }
}